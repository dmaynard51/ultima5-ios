    public Font(GraphicsDevice gd)
    {
        int n = Last - First + 1;
        var px = new Color[n * Glyph * Glyph];
        for (int ci = 0; ci < n; ci++)
            for (int y = 0; y < Glyph; y++)
            {
                byte bits = Rows[ci][y];
                for (int x = 0; x < Glyph; x++)
                    if ((bits & (1 << x)) != 0)
                        px[y * (n * Glyph) + ci * Glyph + x] = Color.White;
            }
        _atlas = new Texture2D(gd, n * Glyph, Glyph);
        _atlas.SetData(px);
    }

    public int Width(string s, int scale) => s.Length * Glyph * scale;

    public void Draw(SpriteBatch sb, string text, int x, int y, int scale, Color color)
    {
        foreach (char ch in text)
        {
            if (ch >= First && ch <= Last)
            {
                int ci = ch - First;
                sb.Draw(_atlas, new Rectangle(x, y, Glyph * scale, Glyph * scale),
                        new Rectangle(ci * Glyph, 0, Glyph, Glyph), color);
            }
            x += Glyph * scale;
        }
    }
